% Create a vector of numbers
vector = [12, 45, 23, 67, 9, 31, 54, 18];

% Calculate the maximum value in the vector
maximum_value = max(vector);

% Calculate the minimum value in the vector
minimum_value = min(vector);

% Calculate the sum of all values in the vector
sum_of_values = sum(vector);

% Display the results
fprintf('Vector of numbers: %s\n', mat2str(vector));
fprintf('Maximum value: %d\n', maximum_value);
fprintf('Minimum value: %d\n', minimum_value);
fprintf('Sum of values: %d\n', sum_of_values);
