color_user = input('Enter the color Number : \n');
color = color_user;
 switch color
     case 1 
         fprintf('Red color\n');
     case 2
         fprintf('Yellow color\n');
     case 3 
         fprintf('Blue color\n');
     case 4
         fprintf('white color\n');
     case 5
         fprintf('Black color\n');
     otherwise
         fprintf('Invalid code color value\n');
 end